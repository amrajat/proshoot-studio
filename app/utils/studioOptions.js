/**
 * Returns clothing options based on the selected gender.
 * - 'man': Returns only men's clothing.
 * - 'woman': Returns only women's clothing.
 * - 'non-binary': Returns a combined, unique list of men's and women's clothing.
 * @param {string} gender - The selected gender ('man', 'woman', 'non-binary').
 * @returns {{options: object, allOptions: Array<object>}} - The clothing options and a flattened array of all options.
 */
export const getClothingOptions = (gender = "default") => {
  let options = {};

  if (gender === "man") {
    options = CLOTHING_OPTIONS_MAN;
  } else if (gender === "woman") {
    options = CLOTHING_OPTIONS_WOMAN;
  } else {
    // For non-binary or default, merge man and woman options, ensuring uniqueness.
    const combined = {};
    const itemNames = new Set();

    Object.entries(CLOTHING_OPTIONS_MAN).forEach(([theme, items]) => {
      if (!combined[theme]) combined[theme] = [];
      items.forEach((item) => {
        if (!itemNames.has(item.name)) {
          combined[theme].push(item);
          itemNames.add(item.name);
        }
      });
    });

    Object.entries(CLOTHING_OPTIONS_WOMAN).forEach(([theme, items]) => {
      if (!combined[theme]) combined[theme] = [];
      items.forEach((item) => {
        if (!itemNames.has(item.name)) {
          combined[theme].push(item);
          itemNames.add(item.name);
        }
      });
    });
    options = combined;
  }

  const allOptions = Object.values(options).flat();
  return { options, allOptions };
};

// Helper function to find the theme for a given clothing item name.
export const findClothingTheme = (itemName) => {
  const allClothingOptions = {
    ...CLOTHING_OPTIONS_MAN,
    ...CLOTHING_OPTIONS_WOMAN,
  };
  for (const theme in allClothingOptions) {
    if (allClothingOptions[theme].some((option) => option.name === itemName)) {
      return theme;
    }
  }
  return "Unknown";
};

// Helper function to find the theme for a given background item name.
export const findBackgroundTheme = (itemName) => {
  for (const theme in BACKGROUND_OPTIONS) {
    if (BACKGROUND_OPTIONS[theme].some((option) => option.name === itemName)) {
      return theme;
    }
  }
  return "Unknown";
};
